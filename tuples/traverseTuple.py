newTuple = (
  'a',
  'b',
  'c',
  'd',
  'e'
)

for i in range(len(newTuple)):
  print(newTuple[i])


"""  
Time Complexity: O(n)
We have to visit each and every element of the tuple, n number of elements

Space Complexity: O(1)
Additional memory for the tuple is not required when traversing its elements

"""